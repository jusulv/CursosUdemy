let a = 3 , b = 2;

//Operador de suma
let z = a + b;
console.log("Resultado de la suma: " + z);

//Operador de resta
z = a - b;
console.log("Resultado de la resta: " + z);

//Operador multiplicación
z = a * b;
console.log("Resultado de la multiplicación: " + z);

//Operador de división
z = a / b;
console.log('Resultado de la división' + z);

//Operador de modulo (residuo)
z = a % b; //Residuo de la división
console.log("Resultado de operación modulo (residuo): " + z);

//Operador de exponente
z = a ** b;
console.log("Resultado de operador exponente: " + z);

//Operador de incremento
//Pre-incremento (el operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

//Post-incremento (el operador ++ después de la variable)
z = b++;
console.log(b);
console.log(z);

//decremento
//Pre-decremento
z= --a;
console.log(a);
console.log(z);

//Postdecremento
z = b--;
console.log(b);
console.log(z);


