let a = 3, b = 2 , c = "3";

//Operadores de comparación
let z = a == c; //Se revisa el valor sin importar el tipo
console.log(z);

z = a === c;  // revisa si los valores son iguales, pero también los tipos
console.log(z);

z = a != c; //Preguntar si son diferentes
console.log(z)

z = a !== c; //Aunque tienen los mismos valores, no tienen el mismo tipo
console.log(z);

z = a < b;
console.log(z);

z = a <= b;
console.log(z);

z = a > b;
console.log(z);

z = a >= b;
console.log(z);

